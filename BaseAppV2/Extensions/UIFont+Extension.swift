import Foundation

// MARK: - Application Fonts
extension UIFont {
    
    /// Main font used in the application with font size of ten pixels.
    static var mainSmall: UIFont { return UIFont.systemFont(ofSize: 10.0) }
    
    /// Main font used in the application with font size of seventeen pixels.
    static var mainMedium: UIFont { return UIFont.systemFont(ofSize: 17.0) }
    
    /// Main font used in the application with font size of twenty-five pixels.
    static var mainLarge: UIFont { return UIFont.systemFont(ofSize: 25.0) } 
}
